public class Student {
    String name;
    int roll_no, marks1, marks2, marks3;

    public Student(String name, int roll_no, int marks1, int marks2, int marks3) {
        this.name = name;
        this.roll_no = roll_no;
        this.marks1 = marks1;
        this.marks2 = marks2;
        this.marks3 = marks3;
    }
}